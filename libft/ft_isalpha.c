#include "libft.h"

int	ft_isalpha(int c)
{
	unsigned char	i;

	i = (unsigned char)c;
	if ((c >= 65 && c <= 90) || (c >= 97 && c <= 122))
	{
		return (1);
	}
	return (0);
}
